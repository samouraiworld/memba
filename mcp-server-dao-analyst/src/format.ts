export function formatRiskAssessment(
  result: {
    riskLevel: string;
    risks: Array<{
      category: string;
      description: string;
      severity: string;
      mitigation: string;
    }>;
    overallAssessment: string;
  },
  proposalId: string,
  realmPath: string,
  model: string
): string {
  const lines: string[] = [];

  lines.push(`# Risk Assessment — Proposal #${proposalId}`);
  lines.push(`**DAO:** \`${realmPath}\` | **Model:** ${model}`);
  lines.push("");
  lines.push(`## Overall Risk: ${result.riskLevel.toUpperCase()}`);
  lines.push("");
  lines.push(result.overallAssessment);

  if (result.risks.length > 0) {
    lines.push("");
    lines.push("## Identified Risks");
    lines.push("");
    lines.push("| Category | Severity | Description | Mitigation |");
    lines.push("|----------|----------|-------------|------------|");
    for (const r of result.risks) {
      lines.push(
        `| ${r.category} | ${r.severity} | ${r.description} | ${r.mitigation} |`
      );
    }
  }

  lines.push("");
  lines.push("---");
  lines.push("*AI-generated risk assessment. Verify against on-chain data.*");

  return lines.join("\n");
}

function capitalize(s: string): string {
  return s.charAt(0).toUpperCase() + s.slice(1);
}
